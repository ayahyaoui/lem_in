#include "libft.h"

int		ft_isspace(int i)
{
	char	c;

	c = (char)i;
	if (c == '\t' || c == '\n' || c == '\v' || c == '\f' || c == '\r'
			|| c == ' ')
		return (1);
	return (0);
}
