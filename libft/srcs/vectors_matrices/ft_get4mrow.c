#include "libft.h"

t_4vect		ft_get4mrow(double m[4][4], int row)
{
	t_4vect hor;

	hor.x = m[row][0];
	hor.y = m[row][1];
	hor.z = m[row][2];
	hor.w = m[row][3];
	return (hor);
}
